export function getColor(rate: number): string {
  if (rate >= 55) return "#00ff88";
  if (rate >= 40) return "#88ff00";
  if (rate >= 25) return "#ffee00";
  if (rate >= 12) return "#ff8800";
  return "#ff3300";
}

export function getColorRgb(rate: number): [number, number, number] {
  if (rate >= 55) return [0, 255, 136];
  if (rate >= 40) return [136, 255, 0];
  if (rate >= 25) return [255, 238, 0];
  if (rate >= 12) return [255, 136, 0];
  return [255, 51, 0];
}
